function setup() { // telling this bad boy to get going

    createCanvas( 1000, 800) // This tells the code to create a place where we can put everything

    background(215, 183, 64) //This tells us what color the canvas will be
                             // You can put actual color names like 'red' or just use CMYK color codes

}
function draw() {
  textSize(10) //font size of text
  text("What up fam", 500, 400) //telling p5 to print the text "what up fam" from the center of our canvas (x axis, y axis)
}
